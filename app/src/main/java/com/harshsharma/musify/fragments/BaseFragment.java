package com.harshsharma.musify.fragments;

import android.content.Context;
import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.fragment.app.Fragment;

import com.harshsharma.musify.activities.BaseActivity;
import com.harshsharma.musify.interfaces.MusifyConstants;


//All fragments in the app must extend this Fragment
public abstract class BaseFragment extends Fragment implements MusifyConstants {

    protected Context context;

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        this.context = context;
    }

    @Override
    public void onCreate(@Nullable Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container,
                             @Nullable Bundle savedInstanceState) {
        return super.onCreateView(inflater, container, savedInstanceState);
    }

    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
    }

    @Override
    public void onDetach() {
        super.onDetach();
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
    }

    @Override
    public void onStop() {
        super.onStop();
    }

    @Override
    public void onPause() {
        super.onPause();
    }

    @Override
    public void onResume() {
        super.onResume();
    }

    @Override
    public void onStart() {
        super.onStart();
    }

    public abstract String getFragmentName();

    protected void showToast(String toastMessage) {
        ((BaseActivity) context).showToast(toastMessage);
    }

    protected void showProgress(String progressMessage) {
        ((BaseActivity) context).showProgress(progressMessage);
    }

    protected void dismissProgress() {
        ((BaseActivity) context).dismissProgress();
    }
}
